CREATE DEFINER=`root`@`localhost` PROCEDURE `update_loyer`()
BEGIN
        DECLARE done INT DEFAULT 0;
        DECLARE var_id int;
       DECLARE var_id_releve int;
       DECLARE var_date DATE;
        DECLARE var_montant double(10,2);
        DECLARE curseur1 CURSOR FOR SELECT id, montant,date,id_releve FROM releve_detail 
                                                            WHERE montant >= 200 AND id_cat1=4;
        DECLARE CONTINUE HANDLER FOR SQLSTATE '02000' SET done = 1;
 
        OPEN curseur1; # ouverture du curseur1
 
        REPEAT
                FETCH curseur1 INTO var_id, var_montant,var_date,var_id_releve;
                IF done = 0 THEN
                        UPDATE releve_detail SET montant=var_montant-200,libelle=CONCAT('loyer+',libelle) WHERE id=var_id;
                        INSERT INTO releve_detail(libelle,montant,type,id_cat1,id_cat2,date,id_releve)
                        VALUE ('loyer',200,'DEBIT',4,7,var_date,var_id_releve);
                END IF;
        UNTIL done
        END REPEAT;
 
        CLOSE curseur1; # fermeture du curseur1
END$$

